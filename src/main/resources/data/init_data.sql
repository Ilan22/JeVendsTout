-- Création de la procédure inserer catégorie
create or replace Procedure InsererCategorie
(
    nom_categorie IN CATEGORIE.NOM%TYPE
) IS
    id_categorie CATEGORIE.ID%TYPE;
BEGIN
SELECT CATEGORIE_SEQ.nextval into id_categorie FROM DUAL;

INSERT INTO CATEGORIE (ID, NOM)
VALUES (id_categorie, nom_categorie);

COMMIT;
end InsererCategorie;

-- Création de la procédure inserer client
create Procedure InsererArticle
(
    nom_article IN ARTICLE.NOM%TYPE,
    categorieId_article IN ARTICLE.CATEGORIE_ID%TYPE
) IS
    id_article ARTICLE.ID%TYPE;
BEGIN
SELECT ARTICLE_SEQ.nextval into id_article FROM DUAL;

INSERT INTO ARTICLE (ID, NOM, CATEGORIE_ID)
VALUES (id_article, nom_article, categorieId_article);

COMMIT;
end InsererArticle;

